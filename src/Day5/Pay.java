package Day5;

public class Pay {
	
	private int basePay = 500000;
	private int rankPay = 100000;
	private int overPay = 30000;
	private int contractPay = 2000000;
	
	public int getBasePay() {
		return basePay;
	}
	public void setBasePay(int basePay) {
		this.basePay = basePay;
	}
	public int getRankPay() {
		return rankPay;
	}
	public void setRankPay(int rankPay) {
		this.rankPay = rankPay;
	}
	public int getOverPay() {
		return overPay;
	}
	public void setOverPay(int overPay) {
		this.overPay = overPay;

	}
	public int getContractPay() {
		return contractPay;
	}
	public void setContractPay(int contractPay) {
		this.contractPay = contractPay;
	}
	
}
